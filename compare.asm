    MOV AX, Y   ; FALSE

  N1:
    CMP AX, Z   ; AX ? Z
    JGE N2      ; AX > Z
    MOV AX, Z   ; FALSE
  
  N2:
    MOV MAX, AX
  
  MOV AX, 4CH
  INT 21H
CSEG ENDS
END MAIN
